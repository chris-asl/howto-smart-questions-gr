            <div>
              <div>
                <h3 class="title">
                  <a id="forum" name="forum"></a>Διάλεξε το forum σου προσεκτικά
                </h3>
              </div>
            </div>
          </div>
          <p>
            Να είσαι ευαίσθητος στην επιλογή του τόπου που θα κάνεις την ερώτησή σου.
            Συνήθως θα σε αγνοήσουν, ή θα σε ξεγράψουν ως χαμένο, αν:
          </p>
          <div class="itemizedlist">
            <ul>
              <li>
                <p>
                  στείλεις την ερώτησή σου σε κάποιο forum που είναι εκτός θέματος
                </p>
              </li>
              <li>
                <p>
                  στείλεις την πολύ στοιχειώδη ερώτησή σου σε κάποιο forum όπου αναμένονται 
                  προχωρημένες τεχνικές ερωτήσεις, ή το αντίστροφο
                </p>
              </li>
              <li>
                <p>
                  στείλεις την ίδια ερώτηση σε υπερβολικά πολλές διαφορετικές ομάδες
                </p>
              </li>
              <li>
                <p>
                  στείλεις ένα προσωπικό e-mail σε κάποιον που ούτε γνωρίζεις, ούτε είναι υπεύθυνος
                  προσωπικά για την λύση του προβλήματός σου
                </p>
              </li>
            </ul>
          </div>
          <p>
            Οι hackers σβήνουν ερωτήσεις που είναι λανθασμένα τοποθετημένες, σε μία προσπάθεια να 
            προστατεύσουν τα επικοινωνιακά τους κανάλια από το να βουλιάξουν μέσα στην ασχετοσύνη. 
            Δεν θέλεις να σου συμβεί αυτό.
          </p>
          <p>
            Το πρώτο βήμα, επομένως, είναι να εντοπίσεις το σωστό forum.
            Και πάλι, το Google και άλλες μέθοδοι αναζήτησης στο Διαδίκτυο είναι φίλοι σου. 
            Χρησιμοποίησέ τους για να βρεις την σελίδα του project που είναι περισσότερο 
            σχετικό με το υλικό ή το λογισμικό που σε δυσκολεύει. Συνήθως θα έχει links προς μία
            λίστα από FAQ (Frequently Asked Questions - Συχνά Υποβαλλόμενες Ερωτήσεις), και προς
            τις mailing lists του project και τα αρχεία τους. Αυτές οι mailing lists είναι το
            τελικό σημείο όπου θα ζητήσεις βοήθεια, αν οι δικές σου προσπάθειες (συμπεριλαμβανομένης
            και της <span class="emphasis"><em>ανάγνωσης</em></span> αυτών των FAQ που βρήκες) 
            δεν σου βρίσκουν μία λύση. Η σελίδα του project ίσως περιγράφει κάποια διαδικασία 
            αναφοράς σφαλμάτων, ή μπορεί να έχει σύνδεσμο προς κάποια. Αν ναι, ακολούθησέ τον.
          </p>
          <p>
            Το να ρίξεις ένα e-mail σε ένα άτομο ή forum το οποίο δεν γνωρίζεις είναι στην καλύτερη
            περίπτωση ρίσκο. Για παράδειγμα, μην υποθέτεις ότι ο συγγραφέας μίας σελίδας με χρήσιμες
            πληροφορίες θέλει να γίνει ο εθελοντής σύμβουλός σου. Μην κάνεις αισιόδοξες προβλέψεις
            σχετικά με τον αν η ερώτησή σου θα είναι ευπρόσδεκτη — αν δεν είσαι σίγουρος, στείλ' την
            αλλού, ή ματαίωσε την αποστολή πλήρως.
          </p>
          <p>
            Όταν επιλέγεις ένα Διαδικτυακό forum, ομάδα ή mailing list, μην εμπιστεύεσαι το όνομα
            το ίδιο εκτενώς — ψάξε για ένα FAQ ή χάρτη για να επιβεβαιώσεις ότι η ερώτησή σου είναι
            επί του θέματος. Διάβασε κάποια προηγούμενα μηνύματα πριν στείλεις, για να αποκτήσεις
            μία αίσθηση για το πώς λαμβάνουν χώρα τα πράγματα εκεί. Στην πραγματικότητα, είναι μία
            πολύ καλή ιδέα να κάνεις μία αναζήτηση με βάση λέξεις-κλειδιά για λέξεις που σχετίζονται με
            το πρόβλημά σου στην ομάδα ή στο αρχείο της mailing list πριν να ρωτήσεις. Μπορεί να σου
            βρει μία απάντηση, και αν όχι, θα σε βοηθήσει να διατυπώσεις καλύτερα την ερώτηση.
          </p>
          <p>
            Μην πολυβολείς όλα τα διαθέσιμα κανάλια βοήθειας με τη μία, αυτό είναι σαν να ουρλιάζεις
            και ενοχλεί τους ανθρώπους. Πάρ' τα με τη σειρά, ήρεμα.
          </p>
          <p>
            Γνώριζε ποιο είναι το θέμα σου! Ένα από τα κλασσικά λάθη είναι
            το να κάνεις ερωτήσεις σχετικές με το προγραμματιστικό API σε Unix ή
            σε Windows σε ένα forum αφιερωμένο σε μία γλώσσα ή βιβλιοθήκη που είναι
            συμβατή ανάμεσα και στα δύο. Αν δεν καταλαβαίνεις γιατί αυτό είναι πατάτα,
            καλύτερα να μην κάνεις τίποτε μέχρι να το πιάσεις.
          </p>
          <p>
            Γενικότερα, ερωτήσεις σε ένα προσεκτικά διαλεγμένο δημόσιο forum
            είναι περισσότερο πιθανόν να απαντηθούν με χρήσιμες απαντήσεις απ' ότι 
            οι ισοδύναμες ερωτήσεις σε ένα ιδιωτικό. Υπάρχουν πολλαπλοί λόγοι
            γι' αυτό. Ένας είναι απλά το μέγεθος του σώματος πιθανών ανταποκριτών.
            Ένας άλλος είναι το μέγεθος του κοινού — οι hackers προτιμούν να απαντούν σε ερωτήσεις
            που εκπαιδεύουν πληθώρα ανθρώπων αντί ερωτήσεων που υπηρετούν μόνο λίγους.
          </p>
          <p>
            Αναμενόμενα, οι ικανοί hackers και οι συγγραφείς δημοφιλούς λογισμικού
            ήδη λαμβάνουν περισσότερο απ' το δίκαια αναλογούμενο ποσοστό όσον αφορά τον όγκο των 
            λανθασμένα κατατοπισμένων ερωτήσεων. Για να προστεθεί σε αυτή
            την πλημμύρα, μπορεί σε ειδικές περιπτώσεις να έρθει η δική σου σταγόνα η οποία θα 
            ξεχειλίσει το ποτήρι — αρκετές φορές, άτομα που είχαν συμβάλλει σε δημοφιλή projects 
            απέσυραν την υποστήριξή τους λόγω παράπλευρων ζημιών σε μορφή άχρηστου όγκου 
            e-mail προς τις προσωπικές τους διευθύνσεις που έγινε αφόρητη.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="usefora" name="usefora"></a>Τα forums του Παγκόσμιου Ιστού και του IRC που απευθύνονται σε αρχάριους συχνά δίνουν τις πιο γρήγορες απαντήσεις
                </h3>
              </div>
            </div>
          </div>
          <p>
            Η τοπική σου ομάδα χρηστών, ή η διανομή του Linux σου, μπορεί
            να διαφημίζει ένα forum του Παγκόσμιου Ιστού ή ένα κανάλι στο IRC όπου οι αρχάριοι 
            μπορούν να λάβουν βοήθεια. (Σε μη αγγλόφωνες χώρες, τα forums αρχαρίων είναι πιο πιθανό
            να είναι mailing lists. [ΣτΜ: Αυτό δεν είναι απαραίτητα αλήθεια. Στην ελληνική κοινότητα
            ελεύθερου λογισμικού υπάρχουν εξίσου forums, mailing lists, wikis, και κανάλια στο IRC
            από όπου κάποιος μπορεί να λάβει βοήθεια.])
            Αυτά είναι καλά μέρη για να ρωτήσεις, ειδικά αν πιστεύεις ότι έχεις σκοντάψει πάνω σε 
            ένα σχετικά απλό ή συνηθισμένο πρόβλημα. Ένα διαφημιζόμενο κανάλι στο IRC αποτελεί μία
            ανοιχτή πρόσκληση να κάνεις ερωτήσεις σε αυτό και συχνά να λάβεις απαντήσεις σε 
            πραγματικό χρόνο.
          </p>
          <p>
            Στην πραγματικότητα, αν έχεις πάρει το πρόγραμμα το οποίο σου δημιουργεί προβλήματα από 
            μία διανομή του Linux (όπως είναι συνηθισμένο σήμερα), ίσως να ήταν καλύτερο να ρωτήσεις
            στο forum ή στη λίστα της διανομής πριν να δοκιμάσεις το forum ή τη λίστα του project 
            του προγράμματος. Οι hackers του project μπορεί απλώς να πουν "<span class="quote">χρησιμοποίησε το <span class="emphasis"><em>δικό μας</em></span> build</span>".
          </p>
          <p>
            Πριν να γράψεις σε οποιοδήποτε forum του Παγκόσμιου Ιστού, έλεγξε αν έχει την δυνατότητα
            Αναζήτησης. Αν ναι, δοκίμασε κάμποσες αναζητήσεις με λέξεις-κλειδιά για κάτι παρόμοιο με
            το πρόβλημά σου — μπορεί απλώς να βοηθήσει. Αν έκανες μία αναζήτηση στον Παγκόσμιο Ιστό
            προηγουμένως (όπως θα άρμοζε), αναζήτησε μέσα στο forum ούτως ή άλλως — η μηχανή 
            αναζήτησης του Παγκόσμιου Ιστού που χρησιμοποιείς μπορεί να μην έχει όλο το forum 
            απαριθμημένο πρόσφατα.
          </p>
          <p>
            Υπάρχει μια αυξανόμενη τάση  για projects να κάνουν υποστήριξη χρηστών μέσω των 
            forum του Παγκόσμιου Ιστού ή των καναλιών στο IRC, με τα e-mail να δεσμεύονται 
            περισσότερο για όγκο σχετικό με την ανάπτυξη του project. Οπότε ψάξε για αυτά τα κανάλια
            πρώτα όταν αναζητάς βοήθεια σχετική με ένα συγκεκριμένο project.
          </p>
        </div>
