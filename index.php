            </dd>
          </dl>
        </div>
        <div class="variablelist">
          <dl>
            <dt>
              <span class="term"><span class=
              "strong"><strong>Ηλίθιο:</strong></span> Δεν μπορώ να κάνω τον κώδικα από το project κοκο
