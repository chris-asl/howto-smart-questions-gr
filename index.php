          <p>
            Αν ένα project έχει δύο mailing lists
            ή forums "<span class="quote">χρήστη</span>"
            και "<span class=
            "quote">προγραμματιστή</span>" (ή
            "<span class=
            "quote">hacker</span>"), και εσύ δεν κάνεις hacking πάνω στον κώδικα, ρώτα στο forum ή
            στη λίστα "<span class=
            "quote">χρήστη</span>". Μην υποθέτεις ότι θα είσαι ευπρόσδεκτος στην λίστα των 
            προγραμματιστών, όπου πιθανότητα θα βιώσουν την ερώτησή σου ως θόρυβο ανάμεσα στον όγκο
            δεδομένων σχετικά με την ανάπτυξη του προγράμματος.
          </p>
          <p>
            Εντούτοις, αν είσαι <span class=
            "emphasis"><em>σίγουρος</em></span> ότι η ερώτησή σου είναι μη τετριμμένη, και δεν 
            λαμβάνεις κάποια απάντηση στην λίστα/forum "<span class=
            "quote">χρήστη</span>" για πολλαπλές ημέρες, δοκίμασε αυτή του "<span class=
            "quote">προγραμματιστή</span>". Θα σας προτείναμε ανεπιφύλακτα να γλυστρήσετε αθόρυβα
            εκεί μέσα για μερικές ημέρες πριν να αποστείλετε κάτι για να γνωρίσετε τα τοπικά ήθη και 
            έθιμα (στην πραγματικότητα αυτή είναι μία καλή συμβουλή για οποιαδήποτε ιδιωτική ή 
            ημι-ιδιωτική λίστα).
          </p>
          <p>
            Αν δεν μπορείς να βρεις την διεύθυνση μίας mailing list του project, αλλά βλέπεις μόνο
            την διεύθυνση του διαχειριστή του project, προχώρα και γράψε στον διαχειριστή. Αλλά ακόμη
            και σε αυτή την περίπτωση, μην υποθέτεις ότι η mailing list δεν υπάρχει. Ανέφερε στο 
            e-mail σου ότι δοκίμασες και δεν μπόρεσες να βρεις την κατάλληλη mailing list. Επίσης
            ανέφερε ότι δεν έχεις αντίρρηση με το να προωθηθεί το μήνυμά σου σε άλλους ανθρώπους. 
            (Πολλοί άνθρωποι πιστεύουν ότι ένα ιδιωτικό e-mail πρέπει να παραμένει ιδιωτικό, ακόμη
            και αν δεν υπάρχει τίποτα μυστικό μέσα του. Επιτρέποντας την προώθηση του μηνύματός σου
            δίνεις στον συνομιλητή σου μία επιλογή σχετικά με τον χειρισμό του e-mail σου.)
          </p>
