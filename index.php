            γενικότερο ενδιαφέρον σε μία mailing list ή ομάδα, είναι πρέπον να στείλεις την
            απάντηση αυτή εκεί.
          </p>
          <p>
            Βέλτιστα, η απάντηση αυτή θα πρέπει να είναι στο αρχικό thread που ξεκίνησε με την 
            αποστολή της αρχικής ερώτησης, και θα πρέπει να έχει κάτι όπως 
            `ΔΙΟΡΘΩΘΗΚΕ',
            `ΛΥΘΗΚΕ' ή κάποια εξίσου προφανή ετικέτα στην γραμμή θέματος. Σε μία mailing
