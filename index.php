                <h3 class="title">
                  <a id="bespecific" name="bespecific"></a>Χρησιμοποίησε συγκεκριμένες επικεφαλίδες 
                  θέματος που να βγάζουν νόημα
                </h3>
              </div>
            </div>
          </div>
          <p>
            Στις mailing lists, τις ομάδες ή στα forums του Παγκόσμιου Ιστού, η επικεφαλίδα θέματος
            είναι η χρυσή σου ευκαιρία να προσελκύσεις ειδικούς με τα κατάλληλα προσόντα σε 50 ή 
            λιγότερους χαρακτήρες.
            Μην τους ξοδεύεις φλυαρώντας όπως στο
            "<span class="quote">Βοηθήστε με</span>" (ας μην μιλήσουμε για το
            "<span class="quote">ΒΟΗΘΗΣΤΕ ΜΕ!!!!</span>" — μηνύματα με θέματα όπως αυτό αγνοούνται
            αυθόρμητα). Μην προσπαθείς να μας εντυπωσιάσεις με την βαθύτητα της αγωνίας σου — 
            χρησιμοποίησε το χώρο για μία υπερ-συνοπτική περιγραφή του προβλήματος αντ' αυτού.
          </p>
          <p>
            Μία καλή σύμβαση για τις επικεφαλίδες θέματος που χρησιμοποιείται από πολλές οργανώσεις
            τεχνικής υποστήριξης είναι η "<span class="quote">αντικείμενο - απόκλιση</span>". Το 
            μέρος "<span class=
            "quote">αντικείμενο</span>" ορίζει ποιο πράγμα ή ποια ομάδα πραγμάτων αντιμετωπίζει 
            πρόβλημα, και το μέρος "<span class="quote">απόκλιση</span>" περιγράφει την απόκλιση
            από την αναμενόμενη συμπεριφορά.
          </p>
          <div class="variablelist">
            <dl>
              <dt>
                <span class="term"><span class=
                "strong"><strong>Ηλίθιο:</strong></span></span>
              </dt>
              <dd>
                <p>
