            <div>
              <h2 class="title" style="clear: both">
                <a id="intro" name="intro"></a>Εισαγωγή
              </h2>
            </div>
          </div>
        </div>
        <p>
          Στον κόσμο των <a href="http://earthlab.uoi.gr/indy/hacker-howto-gr/">hackers</a>, το είδος των απαντήσεων που λαμβάνεις στις τεχνικές
          σου ερωτήσεις εξαρτάται τόσο από τον τρόπο που κάνεις τις ερωτήσεις όσο
          και από την δυσκολία της δημιουργίας της απάντησης. Αυτός ο οδηγός θα σου διδάξει
          πώς να κάνεις ερωτήσεις με τρόπο τέτοιο ώστε να είναι πιο πιθανό να λάβεις 
          ικανοποιητικές απαντήσεις.
        </p>
        <p>
          Τώρα που η χρήση λογισμικού ανοιχτού κώδικα έχει διαδοθεί, μπορείς 
          συχνά να λάβεις εξίσου καλές απαντήσεις από άλλους, έμπειρους χρήστες
          όσο και από hackers. Αυτό είναι ένα Καλό Πράγμα -
          οι χρήστες τείνουν να είναι πιο ανεκτικοί στους τύπους αποτυχίας που έχουν
          συχνά οι νέοι χρήστες. Παρ' όλα αυτά, το να συμπεριφέρεσαι στους 
          έμπειρους χρήστες όπως στους hackers με τον τρόπο που προτείνουμε εδώ
          θα είναι γενικά ο πιο αποδοτικός τρόπος να λάβεις βοηθητικές απαντήσεις
          και από αυτούς.
        </p>
        <p>
          Το πρώτο πράγμα που πρέπει να καταλάβεις είναι ότι στους hackers 
          πράγματι αρέσουν τα δύσκολα προβλήματα και οι καλές ερωτήσεις που
          προωθούν την σκέψη και τα αφορούν. Αν δεν μας άρεσαν, δεν θα ήμασταν εδώ που είμαστε. 
          Αν μας δώσεις μία ενδιαφέρουσα ερώτηση για να σκεφτούμε, θα σου είμαστε ευγνώμονες —
          οι καλές ερωτήσεις είναι καλά ερεθίσματα και δώρα. Οι καλές ερωτήσεις μας βοηθούν
          να αναπτύξουμε την δυνατότητα για κατανόηση,
          και συχνά αποκαλύπτουν προβλήματα που μπορεί να μην είχαμε παρατηρήσει ή σκεφτεί 
          σε άλλη περίπτωση. Ανάμεσα στους hackers, η φράση
          "<span class="quote">Καλή ερώτηση!</span>" είναι μία έντονη και ειλικρινής φιλοφρόνηση.
        </p>
        <p>
          Παρ' όλα ταύτα, οι hackers έχουν την φήμη ότι συναντούν τις απλές ερωτήσεις με κάτι
          που μοιάζει με έχθρα ή υπεροψία. Μερικές φορές φαίνεται σαν να είμαστε ανεξήγητα αναιδείς
          στους αρχάριους και στους αγνοώντες. Αλλά αυτό δεν είναι στ' αλήθεια σωστό.
        </p>
        <p>
          Αυτό που είμαστε, χωρίς να χρειάζεται να απολογηθούμε, είναι εχθρικοί στους ανθρώπους
          που φαίνονται απρόθυμοι να σκεφτούν ή να κάνουν την προεργασία τους πριν να κάνουν ερωτήσεις.
          Άνθρωποι σαν και αυτούς είναι ρουφήχτρες χρόνου — λαμβάνουν χωρίς να δίνουν πίσω, και
          σπαταλούν χρόνο που θα μπορούσαμε να είχαμε ξοδέψει σε κάποια άλλη, πιο ενδιαφέρουσα, ερώτηση
          και κάποιο άλλο άτομο που να άξιζε περισσότερο μία απάντηση. Αυτούς τους ανθρώπους, τους
          αποκαλούμε "<span class=
          "quote">χαμένους</span>", "<span class="quote">losers</span>" (και για ιστορικούς λόγους μερικές φορές το γράφουμε 
          "<span class=
          "quote">lusers</span>").
        </p>
        <p>
          Κατανοούμε ότι υπάρχουν πολλοί άνθρωποι που απλώς θέλουν να χρησιμοποιήσουν το λογισμικό
          που αναπτύσσουμε, και που δεν βρίσκουν ενδιαφέρον στο να μάθουν τεχνικές λεπτομέρειες. Για
          τους περισσότερους ανθρώπους, ένας υπολογιστής είναι μόνο ένα εργαλείο, ένα μέσο για ένα
          σκοπό — έχουν πιο σημαντικά πράγματα να κάνουν και να ζήσουν τη ζωή τους. Αυτό το 
          αναγνωρίζουμε, και δεν αναμένουμε από τον καθένα να αναπτύξει ενδιαφέρον για τα τεχνικά 
          ζητήματα που μας σαγηνεύουν. Εντούτοις, ο τρόπος με τον οποίο απαντάμε σε ερωτήσεις είναι
          συντονισμένος για ανθρώπους που <span class="emphasis"><em>έχουν</em></span> αυτό το 
          ενδιαφέρον και είναι πρόθυμοι να γίνουν ενεργοί συμμετέχοντες στην λύση του προβλήματος. 
          Αυτό δεν πρόκειται να αλλάξει. Και ούτε θά 'πρεπε — αν άλλαζε, θα γινόμασταν λιγότερο
          παραγωγικοί σε αυτό που κάνουμε καλύτερα.
        </p>
        <p>
          Είμαστε (κατά μεγάλο μέρος) εθελοντές. Αφιερώνουμε χρόνο από τις 
          άλλες ασχολίες μας για να απαντήσουμε σε ερωτήσεις, και ορισμένες φορές
          καταποντιζόμαστε από αυτές. Συνεπώς, φιλτράρουμε δίχως έλεος. Συγκεκριμένα, 
          πετάμε στα σκουπίδια ερωτήσεις από ανθρώπους που φαίνονται να είναι χαμένοι
          με σκοπό να ξοδέψουμε τον χρόνο προς απάντηση ερωτήσεων πιο αποδοτικά, για τους νικητές.
        </p>
        <p>
          Αν βρίσκεις τον εαυτό σου αντίθετο με αυτή την συμπεριφορά, 
          αν την θεωρείς μειωτική προς εσένα, ή υπεροπτική, έλεγξε τις εικασίες σου. 
          Δεν σου ζητάμε να υποκλιθείς μπροστά μας — στην πραγματικότητα, οι περισσότεροι
          από εμάς δεν θα ήθελαν τίποτε άλλο παρά να σε αντιμετωπίζουν σαν ίσο, και να σε 
          καλωσορίσουν στην κοινότητά μας, αν καταβάλλεις την προσπάθεια που απαιτείται για
          να γίνει εφικτό. Όμως είναι απλώς μη παραγωγικό για εμάς να προσπαθούμε να βοηθούμε
          ανθρώπους που δεν είναι πρόθυμοι να βοηθήσουν τον εαυτό τους. Είναι εντάξει να αγνοείς — 
          δεν είναι εντάξει να το παίζεις χαζός.
        </p>
        <p>
          Οπότε, ενώ δεν είναι αναγκαίο να είσαι ήδη τεχνικά κατατοπισμένος για να τραβήξεις
          την προσοχή μας, <span class=
          "emphasis"><em>είναι</em></span> αναγκαίο να επιδείξεις το είδος της συμπεριφοράς που 
          οδηγεί στην κατατόπιση
          — με επαγρύπνηση, σκεπτικότητα, παρατηρητικότητα,
          προθυμία να είσαι ενεργός συμμετέχοντας στην ανάπτυξη κάποιας λύσης.
          Αν δεν μπορείς να ζήσεις με αυτό το είδος διακρίσεων, σου προτείνουμε να πληρώσεις
          κάποιον για ένα εμπορικό συμβόλαιο υποστήριξης αντί να ζητάς από hackers να γίνουν 
          εθελοντές δωρητές του προσωπικού τους χρόνου για να σε βοηθήσουν.
        </p>
        <p>
          Αν αποφασίσεις να έρθεις σε εμάς για βοήθεια, δεν θέλεις να είσαι ένας από τους χαμένους.
          Ούτε θες να φαίνεται σαν να ήσουν ένας. Ο καλύτερος τρόπος να λάβεις ταχείες και 
          διαδραστικές απαντήσεις είναι να ρωτήσεις όπως θα έκανε ένα άτομο με εξυπνάδα, 
          αυτοπεποίθηση, και γνώσεις, που απλώς έτυχε να χρειαστεί βοήθεια σχετικά με ένα
          συγκεκριμένο πρόβλημα.
        </p>
        <p>
          (Βελτιώσεις για αυτό τον οδηγό είναι ευπρόσδεκτες. Μπορείς να
          στείλεις προτάσεις στο <a href="mailto:esr@thyrsus.com">esr@thyrsus.com</a> ή στο <a href=
          "respond-auto@linuxmafia.com">respond-auto@linuxmafia.com</a>. Σημείωσε πάραυτα ότι
          αυτό το έγγραφο δεν έχει σκοπό να είναι ένας γενικός οδηγός σχετικά
          με την <a href="http://www.ietf.org/rfc/rfc1855.txt">netiquette</a>, και γενικά θα απορρίψουμε προτάσεις που δεν είναι
          συγκεκριμένα σχετικές με την απόσπαση χρήσιμων απαντήσεων από 
          ένα τεχνικό forum.)
        </p>
      </div>
