            αναφορές σφαλμάτων από το να λάβουν μία ευγενή ασάφεια.
            (Αν αυτό σε προβληματίζει, θυμήσου ότι εκτιμούμε μία ερώτηση από το τι μας διδάσκει.)
          </p>
