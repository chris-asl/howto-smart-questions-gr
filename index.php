            υποστηρίζεται σχεδόν παντού κάτω από ρυθμίσεις όπως 
            "<span class="quote">παρακολούθηση αυτής της συζήτησης</span>",
            "<span class="quote">στείλε e-mail όταν απαντηθεί</span>", κλπ.
          </p>
