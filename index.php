          <p>
            Η διαδικασία του να γράψεις μία περιγραφή τύπου 
            "<span class=
            "quote">αντικείμενο-απόκλιση</span>"
            θα σε βοηθήσει να οργανώσεις τον τρόπο σκέψης σου σχετικά με το πρόβλημα πιο λεπτομερώς. 
            Τι επηρεάζεται; Μόνο ο κέρσορας του ποντικιού ή και άλλα γραφικά επίσης; 
            Συμβαίνει συγκεκριμένα στην X.org έκδοση του X; Στην έκδοση 6.8.1; Συμβαίνει 
            συγκεκριμένα στα chipsets κάρτας γραφικών Κοκοware; Στο μοντέλο MV1005? Ένας hacker που
            βλέπει το αποτέλεσμα μπορεί αμέσως να καταλάβει τι είναι αυτό με το οποίο έχεις πρόβλημα
            <span class="emphasis"><em>και</em></span> το πρόβλημα το οποίο έχεις, με μια ματιά.
          </p>
          <p>
            Γενικότερα, φαντάσου να κοιτάς μία απαρίθμηση από ερωτήσεις, με μόνο την γραμμή θέματος
            να εμφανίζεται. Κάνε την γραμμή θέματος να αντικατοπτρίζει αρκετά καλώς την ερώτησή σου
            ώστε ο επόμενος τύπος που θα αναζητά το αρχείο για μία ερώτηση παρόμοια με την δική σου
            να μπορεί να ακολουθήσει το thread για να φτάσει σε μία απάντηση αντί να στείλει την
            ερώτηση ξανά.
          </p>
          <p>
            Αν κάνεις μία ερώτηση εντός μίας απάντησης, σιγουρέψου ότι αλλάζεις την γραμμή θέματος
            για να επισημάνεις ότι κάνεις μία ερώτηση.
            Μία γραμμή Θέματος που είναι σαν το
            "<span class="quote">Re: δοκιμή</span>" ή
            "<span class="quote">Re: νέο σφάλμα</span>" έχει μικρότερες πιθανότητες να προσελκύσει
            αρκετή προσοχή ώστε να φανεί χρήσιμη. Επιπλέον, ξεκαθάρισε τα αποσπάσματα παλιών 
            μηνυμάτων στο ελάχιστο δυνατό που να βγάζει νόημα αρκετό για να δώσεις ενδείξεις στους 
            νέους αναγνώστες.
          </p>
          <p>
            Μην πατάς απλώς απάντηση σε ένα μήνυμα λίστας για να αρχίσεις ένα εντελώς καινούργιο
            thread. Αυτό θα περιορίσει το κοινό σου. Κάποια προγράμματα ανάγνωσης αλληλογραφίας, 
            όπως το mutt, επιτρέπουν στο χρήστη να ταξινομήσει με βάση το thread και στη συνέχεια
            να αποκρύψει μηνύματα εντός του thread χρησιμοποιώντας αναδίπλωση. Οι τυπάδες που το 
            κάνουν αυτό δεν θα δουν ποτέ το μήνυμά σου.
          </p>
          <p>
            Το να αλλάξεις το θέμα δεν επαρκεί. Το Mutt, και πιθανώς και άλλα προγράμματα ανάγνωσης
            αλληλογραφίας, κοιτάει και για άλλες πληροφορίες στις κεφαλίδες του e-mail για να το 
            εντάξει σε ένα thread, όχι την γραμμή θέματος. Αντ' αυτού, ξεκίνα ένα παντελώς νέο e-mail.
          </p>
          <p>
            Στα forums του Παγκόσμιου Ιστού, οι κανόνες καλής πρακτικής είναι ελαφρώς διαφορετικοί, 
            επειδή τα μηνύματα είναι συνήθως πολύ περισσότερο σφιχτά συνδεδεμένα με συγκεκριμένα
            threads συζητήσεων και συχνά αόρατα έξω από αυτά τα threads. Το να αλλάζεις το θέμα όταν
            κάνεις μία ερώτηση μέσα σε μία ανταπόκριση δεν είναι απαραίτητο. Κάποια forums ούτε καν
            επιτρέπουν διαφοροποιημένη γραμμή θέματος στις απαντήσεις, και σχεδόν κανείς δεν τις 
            διαβάζει ακόμη και όταν επιτρέπεται.
            Παρ' όλα αυτά, το να κάνεις μία ερώτηση μέσα σε μία ανταπόκριση είναι μία αμφισβητήσιμη 
            πρακτική καθεαυτή, επειδή θα το δούν μόνο όσοι παρακολουθούν αυτό το thread. Έτσι, 
            εκτός και αν είσαι σίγουρος ότι <span class="emphasis"><em>θέλεις</em></span> να
            ρωτήσεις μόνο τους ανθρώπους που είναι επί του παρόντος ενεργοί στο thread, ξεκίνα ένα
            νέο.
          </p>
