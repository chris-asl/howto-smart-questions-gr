              </p>
            </dd>
            <dt>
              <span class="term"><span class=
              "strong"><strong>Έξυπνο:</strong></span> Χρησιμοποίησα το Google
              για να ψάξω για το "<span class=
              "quote">Τραλαλα Περίπραγμα 2600</span>"
              στον Παγκόσμιο Ιστό, αλλά δεν βρήκα χρήσιμα πράγματα. Μπορώ να βρω κάποιο σύνδεσμο
              προς πληροφορίες σχετικά με προγραμματισμό σε αυτή τη συσκευή;</span>
            </dt>
            <dd>
