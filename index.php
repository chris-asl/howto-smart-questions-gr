            </dt>
            <dd>
              <p>
                Αυτό το άτομο, από την άλλη, φαίνεται να αξίζει μία απάντηση. Έχει επιδείξει 
                εξυπνάδα επίλυσης προβλημάτων αντί να περιμένει παθητικά για κάποια λύση από τα 
                ουράνια.
              </p>
            </dd>
          </dl>
        </div>
        <p>
          Στην τελευταία ερώτηση, παρατήρησε την υποβόσκουσα αλλά σημαντική διαφορά ανάμεσα στο 
          "<span class="quote">βοηθήστε με</span>" και
          "<span class="quote">Παρακαλώ βοηθήστε με να βρω επιπρόσθετα διαγνωστικά που μπορώ να 
          τρέξω ώστε να καταφέρω να φτάσω στη λύτρωση.</span>"
        </p>
        <p>
          Στην πραγματικότητα, η μορφή της τελευταίας ερώτησης είναι στενά βασισμένη σε ένα 
          πραγματικό γεγονός που συνέβη τον Αύγουστο του 2001 στην mailing list του πυρήνα του 
          linux (linux-kernel mailing list - lkml). Εγώ (ο Eric) ρωτούσα την ερώτηση τότε. Έβλεπα
          μυστήρια κρασαρίσματα σε μία μητρική κάρτα Tyan S2462. Τα μέλη της λίστας με εφοδίασαν με
          τις κρίσιμες πληροφορίες που χρειαζόμουν για να τα λύσω.
        </p>
        <p>
          Κάνοντας την ερώτηση με τον τρόπο που την έκανα, έδωσα κάτι στους ανθρώπους για να 
          απασχοληθούν — το έκανα εύκολο και προσελκυστικό για να ασχοληθούν. Επέδειξα σεβασμό 
          ως προς την ικανότητα των συναδέλφων μου και τους προσκάλεσα να συνεδριάσουν μαζί μου
          ως ίσος προς ίσους. Επέδειξα επιπλέον σεβασμό για την αξία του χρόνου τους λέγοντάς τους
          για τα αδιέξοδα τα οποία ήδη δοκίμασα.
        </p>
        <p>
          Στη συνέχεια, όταν τους ευχαρίστησα όλους και ανέφερα πόσο καλά δούλεψε η διαδικασία, 
          ένα μέλος του lkml παρατήρησε ότι δούλεψε όχι επειδή είμαι ένα 
          "<span class=
          "quote">όνομα</span>" σε εκείνη τη λίστα, αλλά επειδή έκανα την ερώτηση με τη σωστή μορφή.
        </p>
        <p>
          Οι hackers έχουν, όσον αφορά μερικούς από τους τρόπους τους, ένα πολύ άσπλαχνο αξιοκρατικό 
          σύστημα — είμαι σίγουρος ότι είχε δίκιο, και αν <span class=
          "emphasis"><em>είχα</em></span> συμπεριφερθεί σαν σφουγγάρι, θα είχα πολεμηθεί ή αγνοηθεί
          χωρίς να παίζει ρόλο το ποιος είμαι.
          Η προτροπή του να συγγράψω περί ολόκληρου του γεγονότος ως μία εισαγωγή για τους άλλους
          οδήγησε άμεσα στην σύνθεση αυτού του οδηγού.
        </p>
      </div>
