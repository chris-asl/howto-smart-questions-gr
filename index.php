            <div>
              <h2 class="title" style="clear: both">
                <a id="disclaimer" name="disclaimer"></a>Περιορισμός Ευθύνης
              </h2>
            </div>
          </div>
        </div>
        <p>
          Πολλές ιστοσελίδες από projects έχουν συνδέσμους προς αυτό το 
          έγγραφο στις ενότητες σχετικά με την λήψη βοήθειας. Αυτό είναι εντάξει, 
          είναι η χρήση την οποία είχαμε σκοπό να έχει — αλλά αν διατηρείς μία ιστοσελίδα
          και δημιουργείς ένα τέτοιο σύνδεσμο για την σελίδα του project σου, παρακαλώ 
          δημιούργησε μία σαφή σημείωση κοντά στο σύνδεσμο που να δείχνει ότι 
          <span class="emphasis"><em>δεν είμαστε το τμήμα υποστήριξης για το πρόγραμμά σου!</em></span>
        </p>
        <p>
          Μάθαμε, με άσχημο τρόπο, ότι χωρίς μία τέτοια σημείωση, θα
          ενοχλούμαστε συνεχώς από ηλίθιους που θεωρούν ότι έχοντας
          δημοσιεύσει αυτό το έγγραφο κάνει δουλειά μας το να λύσουμε
          όλα τα τεχνικά προβλήματα του κόσμου.
        </p>
        <p>
          Αν διαβάζεις αυτό το έγγραφο επειδή χρειάζεσαι βοήθεια,
          και φεύγεις με την εντύπωση ότι μπορείς να την λάβεις από τους
          συντάκτες αυτού του εγγράφου, <span class=
          "emphasis"><em>εσύ</em></span> είσαι ένας από τους ηλίθιους που αναφέρθηκαν.
          Μην κάνεις σε <span class=
          "emphasis"><em>εμάς</em></span> τις ερωτήσεις. Απλώς θα σε αγνοήσουμε.
          Βρισκόμαστε εδώ για να σου δείξουμε πώς να λάβεις βοήθεια από τους ανθρώπους
          που πραγματικά γνωρίζουν σχετικά με το λογισμικό ή το υλικό το οποίο χειρίζεσαι, 
          όμως στο 99% των περιπτώσεων αυτοί δεν θα είμαστε εμείς. Εκτός και αν ξέρεις <span class=
          "emphasis"><em>σίγουρα</em></span> ότι ένας από τους συντάκτες είναι ειδικός σε αυτό
          που σε ενδιαφέρει, άσε μας ήσυχους και όλοι θα είμαστε χαρούμενοι.
        </p>
