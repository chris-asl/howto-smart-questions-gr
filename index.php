            </div>
          </div>
          <p>
            Όταν ένα project έχει μία mailing list σχετική με την ανάπτυξη, γράψε προς την 
            mailing list, όχι προς τους μεμονωμένους προγραμματιστές, ακόμη και αν πιστεύεις ότι
            γνωρίζεις ποιος μπορεί να απαντήσει καλύτερα στην ερώτησή σου.
            Έλεγξε την τεκμηρίωση του project και την αρχική σελίδα για την διεύθυνση μίας
            mailing list του project, και χρησιμοποίησέ την.
            Υπάρχουν πολλαπλοί καλοί λόγοι για αυτή την πολιτική:
          </p>
          <div class="itemizedlist">
            <ul>
              <li>
                <p>
                  Οποιαδήποτε αρκετά καλή ερώτηση για κάποιον προγραμματιστή θα είναι επίσης 
                  αξιόλογη σε ολόκληρη την ομάδα. Αντιθέτως, αν υποψιάζεσαι ότι η ερώτησή σου είναι
                  υπερβολικά χαζή για την mailing list, αυτό δεν είναι δικαιολογία για την 
                  παρενόχληση ενός μεμονωμένου προγραμματιστή.
                </p>
              </li>
              <li>
                <p>
                  Κάνοντας μία ερώτηση στην λίστα διαχωρίζεται ο φόρτος εργασίας ανάμεσα στους 
                  προγραμματιστές. Ο μεμονωμένος προγραμματιστής (ειδικά αν είναι ο ηγέτης του 
                  project) μπορεί να είναι υπερβολικά απασχολημένος για να απαντήσει στις 
                  ερωτήσεις σου.
                </p>
              </li>
              <li>
                <p>
                  Οι περισσότερες mailing lists αρχειοθετούνται και τα αρχεία απαριθμούνται από 
                  μηχανές αναζήτησης. Αν κάνεις την ερώτησή σου στη λίστα και απαντηθεί, ένας 
                  μελλοντικός εξερευνητής μπορεί να βρει την ερώτησή σου και την σχετική απάντηση 
                  στον Παγκόσμιο Ιστό αντί να την ξαναρωτήσει.
                </p>
              </li>
              <li>
                <p>
                  Αν ορισμένες ερωτήσεις φαίνεται να ρωτώνται συχνά, οι προγραμματιστές μπορούν να
                  χρησιμοποιήσουν αυτού του είδους τις πληροφορίες για να βελτιώσουν την τεκμηρίωση
                  ή το λογισμικό καθεαυτό έτσι ώστε να μην προκαλούν τόση σύγχυση. Αλλά αν αυτές οι
                  ερωτήσεις γίνονται ιδιωτικά, κανένας δεν έχει την πλήρη εικόνα του ποιες ερωτήσεις
                  πραγματοποιούνται πιο συχνά.
                </p>
