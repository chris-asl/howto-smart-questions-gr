        <p>
          Τέλος, θα σας επιδείξω πώς να κάνετε ερωτήσεις με έξυπνο τρόπο μέσω παραδειγμάτων — 
          ζευγάρια ερωτήσεων πάνω στο ίδιο πρόβλημα, μία ερώτηση που γίνεται με ηλίθιο τρόπο και 
