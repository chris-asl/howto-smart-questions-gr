            επιδεικνύει το πρόβλημα. Τι είναι μία ελάχιστη δοκιμαστική περίπτωση; Είναι μία επίδειξη
            του προβλήματος — ίσα ίσα αρκετός κώδικας για να εκθέσει την μη επιθυμητή συμπεριφορά 
            και καθόλου περισσότερο. Πώς να φτιάξεις μία ελάχιστη δοκιμαστική περίπτωση; Αν ξέρεις 
            ποια γραμμή ή ενότητα κώδικα παράγει την προβληματική συμπεριφορά, κάνε ένα αντίγραφό 
            της και πρόσθεσε αρκετό κώδικα υποστήριξης για να παράγεις ένα ολοκληρωμένο παράδειγμα.
            (δηλαδή αρκετό για να γίνει ο πηγαίος κώδικας αποδεκτός στον 
            μεταγλωτιστή/διερμηνέα/οποιαδήποτε εφαρμογή το επεξεργάζεται). Αν δεν μπορείς να 
            το επικεντρώσεις σε μία συγκεκριμένη ενότητα, κάνε ένα αντίγραφο του πηγαίου κώδικα και
            άρχισε να αφαιρείς τμήματα που δεν έχουν να κάνουν με την προβληματική συμπεριφορά. Όσο
            πιο μικρή είναι η ελάχιστη δοκιμαστική σου περίπτωση, τόσο το καλύτερο.
          </p>
          <p>
            Αν απλώς θέλεις κάποιον να αναθεωρήσει τον κώδικά σου, πες το απ' την αρχή,
            και σιγουρέψου ότι αναφέρεις τις περιοχές που νομίζεις ότι συγκεκριμένα χρειάζονται
            αναθεώρηση και γιατί.
          </p>
