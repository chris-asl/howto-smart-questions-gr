            <div>
              <h2 class="title" style="clear: both">
                <a id="before" name="before"></a>Πριν Ρωτήσεις
              </h2>
            </div>
          </div>
        </div>
        <p>
          Πριν κάνεις μία τεχνική ερώτηση μέσω e-mail, ή σε ένα newsgroup, ή σε κάποιο
          chat στον παγκόσμιο ιστό, κάνε τα ακόλουθα:
        </p>
        <div class="procedure">
          <ol>
            <li>
              <p>
                Δοκίμασε να βρεις την απάντηση αναζητώντας στο αρχείο παλιών μηνυμάτων του forum 
                στο οποίο σκοπεύεις να γράψεις.
              </p>
            </li>
            <li>
              <p>
                Δοκίμασε να βρεις την απάντηση αναζητώντας στο Διαδίκτυο.
              </p>
            </li>
            <li>
              <p>
                Δοκίμασε να βρεις την απάντηση διαβάζοντας την τεκμηρίωση.
              </p>
            </li>
            <li>
              <p>
                Δοκίμασε να βρεις την απάντηση διαβάζοντας τις Συχνά Υποβαλλόμενες Ερωτήσεις.
              </p>
            </li>
            <li>
              <p>
                Δοκίμασε να βρεις την απάντηση με μελέτη και πειραματισμό.
              </p>
            </li>
            <li>
              <p>
                Δοκίμασε να βρεις την απάντηση ρωτώντας έναν ικανό φίλο σου.
              </p>
            </li>
            <li>
              <p>
                Αν είσαι προγραμματιστής, δοκίμασε να βρεις την απάντηση διαβάζοντας τον πηγαίο 
                κώδικα.
              </p>
            </li>
          </ol>
        </div>
        <p>
          Όταν υποβάλλεις την ερώτησή σου, δείξε το γεγονός ότι έχεις κάνει αυτά τα πράγματα πρώτα
          — αυτό θα επιβεβαιώσει ότι δεν είσαι ένας τεμπέλης τσιφούτης που ξοδεύει τον χρόνο των 
          ανθρώπων. Ακόμη καλύτερα, δείξε τι <span class=
          "emphasis"><em>έμαθες</em></span> κάνοντας αυτά τα πράγματα. Αρεσκόμαστε στο να απαντάμε
          ερωτήσεις για ανθρώπους που έχουν επιδείξει ότι μπορούν να μάθουν από τις απαντήσεις.
        </p>
        <p>
          Χρησιμοποίησε τακτικές όπως η αναζήτηση στο Google για το κείμενο από το μήνυμα
          σφάλματος που λαμβάνεις (αναζητώντας και στις <a href=
          "http://groups.google.gr/">Ομάδες του Google</a> καθώς και σε ιστοσελίδες). 
          Αυτό ίσως σε πάει κατευθείαν στην τεκμηρίωση σχετικά με την διόρθωση ή σε κάποιο μήνυμα
          ενός mailing list που να απαντάει στην ερώτησή σου. Ακόμη και αν δεν συμβεί κάτι τέτοιο,
          το να πεις "<span class="quote">Έψαξα στο Google για την ακόλουθη φράση αλλά δεν έλαβα
          τίποτα που να μοιάζει ελπιδοφόρο</span>" είναι ένα καλό πράγμα που μπορείς να κάνεις σε ένα
          e-mail ή σε ένα μήνυμα ομάδων όταν ζητάς βοήθεια, μόνο και μόνο επειδή καταγράφει 
          ποιες αναζητήσεις δεν βοηθούν. Θα βοηθήσει επίσης να κατευθυνθούν άλλοι άνθρωποι με 
          παρόμοια προβλήματα στο thread καθώς οι όροι αναζήτησης θα συνδεθούν με αυτό το thread που
          αναφέρεται στο πρόβλημά σου και, αν τα πράγματα πάνε καλά, και στη λύση του. 
        </p>
        <p>
          Χαλαρά. Μην αναμένεις να είσαι σε θέση να λύσεις ένα περίπλοκο πρόβλημα μέσα σε μερικά
          δευτερόλεπτα αναζητήσεων στο Google. Διάβασε και κατανόησε τις Συχνά Υποβαλλόμενες 
          Ερωτήσεις, κάτσε κάτω, χαλάρωσε και σκέψου το πρόβλημα πριν να προσεγγίσεις τους ειδικούς. 
          Δείξε μας εμπιστοσύνη, θα είναι σε θέση να καταλάβουν από τις ερωτήσεις σου πόσο έχεις 
          διαβάσει και πόσο έχεις σκεφτεί, και θα είναι περισσότερο πρόθυμοι να βοηθήσουν αν φτάσεις
          προετοιμασμένος. Μην αρχίζεις να πυροβολείς όλο το οπλοστάσιο ερωτήσεων που μπορεί να έχεις
          μόνο και μόνο επειδή η πρώτη σου αναζήτηση δεν έδωσε αποτελέσματα (ή έδωσε πάρα πολλά).
        </p>
        <p>
          Προετοίμασε την ερώτησή σου. Σκέψου την καλά. Ερωτήσεις που ακούγονται εσπευσμένες θα 
          λάβουν εσπευσμένες απαντήσεις, ή και καθόλου. Όσο περισσότερα κάνεις για να δείξεις ότι
          έχεις αφιερώσει σκέψη και προσπάθεια στο να λύσεις το πρόβλημα πριν να ζητήσεις βοήθεια,
          τόσο περισσότερες πιθανότητες έχεις να λάβεις ουσιαστικά βοήθεια.
        </p>
        <p>
          Πρόσεξε μην κάνεις την λάθος ερώτηση. Αν ρωτήσεις κάποια που βασίζεται σε λανθασμένες
          εικασίες, ο Κ. Τυχαίος Hacker πολύ πιθανόν να απαντήσει με μία άχρηστη λογοτεχνική 
          απάντηση καθώς θα σκέφτεται "<span class="quote">Χαζή ερώτηση...</span>", και θα ελπίζει
          ότι η εμπειρία του να μάθεις σχετικά με αυτό που ρώτησες αντί για αυτό που πραγματικά
          χρειαζόσουν θα σου διδάξει ένα μάθημα.
        </p>
        <p>
          Ποτέ μην υποθέτεις ότι έχεις <span class=
          "emphasis"><em>δικαίωμα</em></span> να λάβεις απάντηση. Δεν έχεις — έτσι κι αλλιώς,
          δεν πληρώνεις για την υπηρεσία. Θα κερδίσεις μία απάντηση, αν την κερδίσεις, ρωτώντας
          μία ουσιαστική, ενδιαφέρουσα ερώτηση που προωθεί την σκέψη — μία που να συμβάλλει έμμεσα
          στην εμπειρία της κοινότητας αντί μόνο να απαιτεί παθητικά γνώσεις από τους άλλους.
        </p>
        <p>
          Από την άλλη πλευρά, κάνοντάς το σαφές ότι είσαι ικανός και πρόθυμος να βοηθήσεις στην 
          διαδικασία ανάπτυξης μίας λύσης είναι μία πολύ καλή αρχή. Ερωτήσεις όπως "<span class=
          "quote">Μπορεί κάποιος να με καθοδηγήσει;</span>",
          "<span class="quote">Τι λείπει από το παράδειγμά μου;</span>", και
          "<span class="quote">Ποια πηγή θα έπρεπε να είχα ελέγξει;</span>" είναι πιο πιθανό
          να απαντηθούν από τις "<span class=
          "quote">Σε παρακαλώ στείλε μου την ακριβή διαδικασία που θα πρέπει να ακολουθήσω.</span>" 
          επειδή το κάνεις ξεκάθαρο ότι είσαι πρόθυμος να ολοκληρώσεις την διαδικασία αν κάποιος
          μπορεί απλώς να σε καθοδηγήσει προς την σωστή κατεύθυνση.
        </p>
      </div>
