            <div>
              <h2 class="title" style="clear: both">
                <a id="id307989" name="id307989"></a>Αν Δεν Μπορείς Να Λάβεις Απάντηση
              </h2>
            </div>
          </div>
        </div>
        <p>
          Αν δεν μπορείς να λάβεις απάντηση, μην το παίρνεις προσωπικά σαν να μην θέλουμε να σε 
          βοηθήσουμε. Μερικές φορές τα μέλη της ομάδα που ρωτήθηκε μπορεί απλώς να μην γνωρίζουν την
          απάντηση. Το να μην υπάρχει απάντηση δεν είναι το ίδιο με το να σε αγνοούν,
          αν και παραδέχομαι πως είναι δύσκολο να δει κάποιος την διαφορά εκ των έξω.
        </p>
        <p>
          Γενικά, απλώς το να ξαναστείλεις την ερώτησή σου είναι κακή ιδέα. Είναι ασκόπως 
          ενοχλητικό. Να έχεις υπομονή: το άτομο με την απάντηση μπορεί να βρίσκεται σε κάποια 
          διαφορετική ζώνη ώρας από την δική σου και να κοιμάται. Ή μπορεί να φταίει το ότι η 
          ερώτησή σου δεν ήταν σωστά μορφοποιημένη από την αρχή.
        </p>
        <p>
          Υπάρχουν άλλες πηγές στις οποίες μπορείς να καταφύγεις, συχνά πηγές που είναι 
          προσαρμοσμένες για της ανάγκες ενός αρχάριου.
        </p>
        <p>
          Υπάρχουν πολλές ομάδες χρηστών online και τοπικές που είναι οπαδοί λογισμικού, ακόμη και
          αν οι ίδιοι ποτέ δεν έχουν γράψει λογισμικό. Αυτές οι ομάδες συχνά δημιουργούνται έτσι 
          ώστε οι άνθρωποι να μπορούν να βοηθούν ο ένας τον άλλον και να βοηθούν νέους χρήστες.
        </p>
        <p>
          Υπάρχουν επίσης πολλαπλές εμπορικές εταιρίες με τις οποίες μπορείς να συνάψεις ένα
          συμβόλαιο για βοήθεια, μεγάλες και μικρές (η Red Hat και η SpikeSource είναι δύο από τις
          πιο γνωστές — υπάρχουν πολλές άλλες). Μην φοβάσαι στην ιδέα του να πρέπει να πληρώσεις 
          για λίγη βοήθεια! Άλλωστε, αν η μηχανή του αυτοκινήτου σου ξεφυσήξει παξιμάδια και βίδες,
          οι πιθανότητες λένε ότι θα το πας σε ένα βουλκανιζατέρ για να σου το φτιάξουν. Επειδή
          το λογισμικό δεν σου κόστισε κάτι, δεν μπορείς πάντα να περιμένεις ότι η υποστήριξη θα
          είναι τζάμπα.
        </p>
        <p>
          Για δημοφιλές λογισμικό όπως το Linux, υπάρχουν τουλάχιστον 10,000 χρήστες ανά προγραμματιστή. 
          Είναι απλώς αδύνατο για ένα άτομο να χειριστεί τις κλήσεις τεχνικής υποστήριξης από 10,000
          χρήστες. Να θυμάσαι ότι ακόμη και όταν πρέπει να πληρώσεις για βοήθεια,
          πληρώνεις πολύ λιγότερο απ' ότι αν έπρεπε να είχες επίσης αγοράσει το λογισμικό 
          (και η υποστήριξη για λογισμικό κλειστού κώδικα είναι συνήθως πιο ακριβή και λιγότερο 
          ικανή απ' ότι η υποστήριξη για λογισμικό ανοιχτού κώδικα).
        </p>
      </div>
