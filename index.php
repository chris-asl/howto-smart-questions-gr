            (εκτός και αν ενδιαφέρεται ο ίδιος ή η ίδια για το Πρόβλημα Χ σε προσωπικό επίπεδο) 
            και μπορεί με αυτόν τον τρόπο να χρησιμοποιήσει τον ίδιο χρόνο για να λύσει ένα 
            διαφορετικό πρόβλημα.
          </p>
          <p>
            Η απάντησή σου δεν χρειάζεται να είναι εκτενής και περιγραφική — ένα απλό
            "<span class="quote">Τι λέει; — τελικά ήταν ένα χαλασμένο καλώδιο δικτύου!
            Σας ευχαριστώ όλους. - Βασίλης</span>" είναι καλύτερο απ' το τίποτα. Στην 
            πραγματικότητα, μία σύντομη και ευχάριστη περίληψη είναι καλύτερη από κάποια εκτενή
            διατριβή εκτός και αν η λύση έχει πραγματικό τεχνικό βάθος. Πες ποια πράξη οδήγησε στη
            λύση του προβλήματος, αλλά δεν χρειάζεται να κάνεις replay σε όλη την διαδικασία που
            ακολούθησες μέχρι να καταλήξεις στην λύση.
          </p>
          <p>
            Για προβλήματα με κάποιο βάθος, είναι αποδεκτό να στείλεις μία περίληψη του ιστορικού
            της διαδικασίας που ακολούθησες για να φτάσεις στη λύση. Περίγραψε τον τελικό
            ορισμό του προβλήματος. Περίγραψε τι δούλεψε ως λύση, και σημείωσε αδιέξοδα που θα 
            ήταν καλό να αποφευχθούν <span class="emphasis"><em>έπειτα</em></span>. Τα αδιέξοδα θα
            πρέπει να έπονται της σωστής λύσης και του άλλου περιληπτικού υλικού, αντί να 
            μετατρέψεις το μήνυμά σου σε αστυνομικό μυθιστόρημα. Ονομάτισε τους ανθρώπους που σε
            βοήθησαν — έτσι θα κάνεις φίλους.
          </p>
          <p>
            Πέρα από το να είσαι ευγενής και περιγραφικός, αυτού του είδους η απάντηση θα βοηθήσει 
            άλλους που ψάχνουν το αρχείο της mailing-list/ομάδας/forum να γνωρίζουν επακριβώς ποια
            λύση σε βοήθησε και συνεπώς μπορεί να βοηθήσει και εκείνους.
          </p>
          <p>
            Τέλος, εξίσου σημαντικός λόγος για αυτού του είδους τις απαντήσεις είναι ότι βοηθάει
            όλους όσους συνεισέφεραν να νιώσουν την ικανοποιητική αίσθηση του κλείσιμου της 
            εκκρεμότητας του προβλήματος. Αν δεν είσαι τεχνοφρικιό ή hacker εσύ ο ίδιος, 
            εμπιστεύσου  μας στο ότι αυτό το συναίσθημα είναι πολύ σημαντικό για τους διδάσκαλους και
            τους έμπειρους χρήστες στους οποίους απευθύνθηκες για βοήθεια.
            
            Οι αφηγήσεις προβλημάτων που καταλήγουν σε ένα άλυτο τίποτα είναι εξοργιστικά πράγματα — 
            οι hackers έχουν μία ακατανίκητη φαγούρα να τις δουν να λύνονται. Η ικανοποίηση που 
            προσφέρει το να ξύσεις αυτή τη φαγούρα θα σου είναι πάρα πολύ βοηθητική την επόμενη φορά
            που θα εισάγεις μία ερώτηση.
          </p>
          <p>
            Εξέτασε το πώς μπορείς να εμποδίσεις τους άλλους από το να έχουν το ίδιο πρόβλημα στο 
            μέλλον. Αναρωτήσου αν ένα patch στην τεκμηρίωση ή στο FAQ θα βοηθούσε, και αν η απάντηση
            είναι ναι, στείλε το patch στον διαχειριστή.
          </p>
          <p>
            Ανάμεσα στους hackers, αυτού του είδους η σωστή συμπεριφορά όσον αφορά τον επίλογο είναι
            στ' αλήθεια πιο σημαντική από συνηθισμένη τυπική ευγένεια.
            Είναι ο τρόπος με τον οποίο λαμβάνεις σεβασμό για το ότι έπαιξες δίκαιο παιχνίδι με τους
            άλλους, το οποίο μπορεί να είναι ένα πολύ αξιόλογο προτέρημα.
          </p>
