          <div>
            <div>
              <h2 class="title" style="clear: both">
                <a id="id308031" name="id308031"></a>Πώς Να Απαντάς Σε Ερωτήσεις με Βοηθητικό Τρόπο
              </h2>
            </div>
          </div>
        </div>
        <p>
          <span class="emphasis"><em>Να είσαι ευγενής.</em></span>
          Το άγχος από κάποιο πρόβλημα μπορεί να κάνει τους ανθρώπους να εμφανίζονται αγενείς ή 
          χαζοί ενώ δεν είναι.
        </p>
        <p>
          <span class="emphasis"><em>Απάντα στον πρωτίστως παραβάτη off-line.</em></span> 
          Δεν υπάρχει ανάγκη για δημόσια ταπείνωση για κάποιον που έκανε ένα ειλικρινές λάθος. Ένας
          πραγματικός αρχάριος μπορεί να μην γνωρίζει πώς να ψάξει μέσα σε αρχεία ή πού 
          αποθηκεύεται και δημοσιεύεται το FAQ.
        </p>
        <p>
          <span class="emphasis"><em>Αν δεν ξέρεις σίγουρα, πες το!</em></span> 
          Μία λάθος αλλά επιτακτική απάντηση είναι χειρότερο και απ' το τίποτα. Μην βάλεις κάποιον
          να κάνει λάθος πράγματα μόνο και μόνο επειδή είναι απλώς διασκεδαστικό να ακούγεσαι σαν 
          ειδικός. Να είσαι μετριόφρον και ειλικρινής — να είσαι το καλό παράδειγμα τόσο για τον
          συνομιλητή σου όσο και για τους ομότιμούς σου.
        </p>
        <p>
          <span class="emphasis"><em>Αν δεν μπορείς να βοηθήσεις, μη σε νοιάζει.</em></span> 
          Μην κάνεις αστεία σχετικά με διαδικασίες που μπορούν να καταστρέψουν την εγκατάσταση του 
          χρήστη —
          κάποιος φτωχούλης ίσως τα ερμηνεύσει ως οδηγίες.
        </p>
        <p>
          <span class="emphasis"><em>Υπέβαλλε αναλυτικές ερωτήσεις για να αποσπάσεις περισσότερες
          λεπτομέρειες.</em></span> Αν είσαι καλός σε αυτό,
          ο συνομιλητής σου θα μάθει κάτι — και μπορεί κι εσύ. Προσπάθησε να μεταμορφώσεις μία 
          κακή ερώτηση σε καλή — να θυμάσαι ότι όλοι ήμασταν αρχάριοι κάποτε.
        </p>
        <p>
          Αν και το να μουρμουρίζεις RTFM είναι δικαιολογημένο όταν απαντάς σε κάποιον που είναι
          τεμπελόγατος, ένας σύνδεσμος προς την τεκμηρίωση (ακόμη και αν είναι απλώς μία πρόταση 
          για αναζήτηση στο google για κάποια λέξη κλειδί) είναι καλύτερος.
        </p>
        <p>
          <span class="emphasis"><em>Αν πρόκειται να απαντήσεις στην ερώτηση, δώσε βάση.</em></span>
          Μην προτρέπεις την χρήση μπακάλικων τρόπων για να κόψει κανείς δρόμο όταν κάποιος 
          χρησιμοποιεί το λάθος εργαλείο ή την λάθος μέθοδο. Πρότρεψε την χρήση καλών εργαλείων. 
          Αναπλαισίωσε την ερώτηση.
        </p>
        <p>
          <span class="emphasis"><em>Βοήθησε την κοινότητα να μάθει από την ερώτηση.</em></span> 
          Όταν βρίσκεσαι αντιμέτωπος με μία καλή ερώτηση,
          ρώτα τον εαυτό σου "<span class="quote">Πώς θα έπρεπε να αλλάξει η αντίστοιχη τεκμηρίωση
          ή το FAQ ώστε να μην χρειαστεί ποτέ να το ξανααπαντήσει κάποιος αυτό;</span>"
          Μετά στείλε ένα patch στον διαχειριστή του εγγράφου.
        </p>
        <p>
          Αν έκανες έρευνα για να απαντήσεις στην ερώτηση, <span class=
          "emphasis"><em>επίδειξε τις ικανότητές σου αντί να γράφεις σαν να είχες βρει την απάντηση
          μυρίζοντας τα νύχια σου.</em></span> Το να απαντάς σε μία καλή ερώτηση είναι σαν να 
          ταΐζεις έναν πεινασμένο ένα πιάτο φαΐ, αλλά αν του διδάξεις την ικανότητα να κάνει έρευνα
          μέσω παραδείγματος είναι σαν να τον διδάσκεις να καλλιεργεί τροφή εφ' όρου ζωής.
        </p>
      </div>
