        </p>
        <p>
          Κάποιες φορές οι άνθρωποι θα σου κατευθύνουν προσωπικές επιθέσεις, θα σε πολεμήσουν δίχως
          λόγο, κλπ., ακόμη και αν δεν τα κάνεις μαντάρα (ή αν τα έκανες μαντάρα μόνο στην φαντασία
          τους). Σε αυτή την περίπτωση, το να παραπονιέσαι είναι ο τρόπος για να κάνεις τα πράγματα
          στ'<span class=
          "emphasis"><em>αλήθεια</em></span> σαλάτα.
        </p>
        <p>
          Αυτοί οι επιτιθέμενοι είναι είτε μπούφοι που δεν έχουν ιδέα αλλά πιστεύουν ότι είναι 
          ειδικοί, ή θέλουν να γίνουν ψυχολόγοι και ελέγχουν αν θα τα κάνεις θάλασσα. Οι υπόλοιποι
          αναγνώστες είτε τους αγνοούν, είτε βρίσκουν τρόπους να τους χειριστούν οι ίδιοι. Η 
          συμπεριφορά των επιτιθέμενων δημιουργεί προβλήματα για τους ίδιους, τα οποία δεν πρέπει
          να σε απασχολούν.
        </p>
        <p>
          Και ούτε να αφήσεις τον εαυτό σου να μπει σε πόλεμο. Το καλύτερο που έχεις να κάνεις με 
          τους πολέμους είναι να τους αγνοήσεις — αφού ελέγξεις ότι είναι στ' αλήθεια πόλεμοι, και 
          όχι σύνδεσμοι προς τους τρόπους με τους οποίους τα θαλάσσωσες, και δεν αποκρυπτογράφησες
          έξυπνα τις απαντήσεις στην πραγματική σου ερώτηση (και αυτό συμβαίνει).
        </p>
      </div>
