              <div>
                <h3 class="title">
                  <a id="writewell" name="writewell"></a>Γράφε καθαρά, γραμματικά και ορθογραφικά σωστά
                </h3>
              </div>
            </div>
          </div>
          <p>
            Ανακαλύψαμε μέσα από την εμπειρία ότι οι άνθρωποι που είναι απρόσεκτοι και 
            τσαπατσούληδες στη συγγραφή είναι συνήθως επίσης απρόσεκτοι και τσαπατσούληδες όταν 
            σκέφτονται και όταν γράφουν κώδικα (αρκετά συχνά τέλος πάντων για να μπορεί κανείς να 
            στοιχηματίζει σε κάτι τέτοιο). Το να απαντάμε σε ερωτήσεις για ανθρώπους που σκέφτονται
            με απρόσεκτο και τσαπατσούλικο τρόπο δεν μας επιβραβεύει — θα προτιμούσαμε να ξοδεύουμε
            τον χρόνο μας αλλού.
          </p>
          <p>
            Έτσι το να εκφράζεις την ερώτησή σου ξεκάθαρα και καλά είναι σημαντικό. Αν δεν μπορείς
            να μπεις στον κόπο να το κάνεις, εμείς δεν μπορούμε να μπούμε στον κόπο να δώσουμε 
            προσοχή. Κατέβαλλε την επιπλέον προσπάθεια για να διανθίσεις τον λόγο σου. Δεν 
            χρειάζεται να είναι στυφός ή επίσημος — στην πραγματικότητα, η κουλτούρα των hacker
            εκτιμά την ανεπίσημη και χιουμοριστική αργκό στο λόγο όταν χρησιμοποιείται με ακρίβεια. 
            Αλλά πρέπει να <span class=
            "emphasis"><em>είναι</em></span> ακριβής — πρέπει να υπάρχει κάποια ένδειξη ότι
            σκέφτεσαι και ότι προσέχεις.
          </p>
          <p>
            Να είσαι ορθογραφημένος και να χρησιμοποιείς την στίξη και την εναλλαγή πεζών-κεφαλαίων 
            με σωστό τρόπο [ΣτΜ: Μην χρησιμοποιείς greeklish αν η τεχνολογία σου το επιτρέπει!]. 
            Μην μπερδεύεις το "<span class=
            "quote">φύλο</span>" με το 
            "<span class=
            "quote">φίλο</span>",
            την "<span class=
            "quote">θνησιμότητα</span>" με την
            "<span class=
            "quote">θνητότητα</span>", ή το 
            "<span class=
            "quote">κυκλοφορικό</span>" με το 
            "<span class=
            "quote">κυκλοφοριακό</span>". Μην ΓΡΑΦΕΙΣ ΑΠΟΚΛΕΙΣΤΙΚΑ ΜΕ ΚΕΦΑΛΑΙΑ — διαβάζεται σαν να 
            ουρλιάζεις και θεωρείται αγένεια.
            (Τα αποκλειστικά-πεζά είναι μόνο ελαφρώς λιγότερο ενοχλητικά, καθώς είναι δύσκολο να 
            διαβαστούν. Ο Alan Cox μπορεί να την βγάλει καθαρή, αλλά εσύ όχι.)
          </p>
          <p>
            Γενικότερα, αν γράφεις σαν να είσαι ένας αμόρφωτος βλάκας το πιο πιθανό είναι να 
            αγνοηθείς. Οπότε μην χρησιμοποιείς τις συντομογραφίες του instant-messaging. Το να 
            γράφεις το "που" ως "π" σε κάνει να φαίνεσαι σαν ένας αμόρφωτος βλάκας μόνο και μόνο για
            να σωθείς από το να πληκτρολογήσεις δύο επιπλέον γράμματα. Χειρότερο: το να γράφεις σΑν
            εΝα χΑζΟ πΑιΔαΚι πΟυ νΟμιζΕι οΤι κΑτΙ κΑνΕι είναι το απόλυτο φιλί της προδοσίας και σου
            εγγυάται ότι δεν θα λάβεις τίποτα πέραν νεκρικής σιωπής 
            (ή, στην καλύτερη περίπτωση, μία αρλούμπα από κοσμητικά επίθετα και ειρωνεία) για 
            αντάλλαγμα.
          </p>
          <p>
            Αν κάνεις ερωτήσεις σε ένα forum που δεν χρησιμοποιεί την μητρική σου γλώσσα, θα λάβεις
            ένα πιο περιορισμένο όγκο παραπόνων για ορθογραφικά και γραμματικά λάθη — αλλά δεν θα
            λάβεις επιπλέον παράπονα για τεμπελιά (και ναι, συνήθως μπορούμε να καταλάβουμε τη 
            διαφορά). Ακόμη, εκτός και αν ξέρεις τις γλώσσες του συνομιλητή σου, γράφε στα Αγγλικά. 
            Οι απασχολημένοι hackers τείνουν να διαγράφουν τις ερωτήσεις σε γλώσσες που δεν 
            καταλαβαίνουν, και τα Αγγλικά είναι η λειτουργική γλώσσα του Διαδικτύου. Με το να 
            γράφεις στα Αγγλικά, ελαχιστοποιείς τις πιθανότητες οι ερωτήσεις σου να πεταχτούν στα
            σκουπίδια αδιάβαστες.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="formats" name="formats"></a>Στέλνε ερωτήσεις σε προσιτή, πρότυπη μορφή
                </h3>
              </div>
            </div>
          </div>
          <p>
            Αν φτιάξεις την ερώτησή σου τεχνικά δύσκολο να διαβαστεί, είναι πιθανό να προσπεραστεί
            προς χάριν κάποιας που δεν είναι. Έτσι:
          </p>
          <div class="itemizedlist">
            <ul>
              <li>
                <p>
                  Να στέλνεις mail σε απλό κείμενο, όχι HTML. (Δεν είναι δύσκολο να 
                  <a href="http://expita.com/nomime.html">απενεργοποιήσεις το HTML</a>.)
                </p>
              </li>
              <li>
                <p>
                  Τα συνημμένα MIME αρχεία είναι συνήθως εντάξει, αλλά μόνο αν αποτελούν ουσιαστικό
                  περιεχόμενο (όπως ένα συνημμένο αρχείο κώδικα ή ένα patch), και όχι σάλτσα που 
                  δημιούργησε το πρόγραμμα αλληλογραφίας σου (όπως ένα επιπλέον αντίγραφο του 
                  μηνύματός σου).
                </p>
              </li>
              <li>
                <p>
                  Μην στέλνεις e-mail στα οποία ολόκληρες παράγραφοι αποτελούνται από μία και 
                  μοναδική αναδιπλωμένη γραμμή. (Το κάνει υπερβολικά δύσκολο να απαντήσει κανείς σε
                  ένα μόνο τμήμα του μηνύματος.)
                  Υπέθεσε ότι οι αναγνώστες σου θα διαβάζουν το mail σε οθόνες που χωρούν 80 
                  χαρακτήρες κειμένου και όρισε την αναδίπλωση κειμένου σου αναλόγως, σε κάτι 
                  μικρότερο του 80.
                </p>
              </li>
              <li>
                <p>
                  Εντούτοις, <span class=
                  "emphasis"><em>μην</em></span> αναδιπλώνεις δεδομένα (όπως αρχεία logs dumps ή
                  μεταγραφές συνόδων) σε κανένα σταθερό μήκος στήλης. Τα δεδομένα πρέπει να 
                  εμπεριέχονται αυτούσια, έτσι ώστε οι συνομιλητές να έχουν την εγγύηση ότι βλέπουν
                  αυτά που είδες.
                </p>
              </li>
              <li>
                <p>
                  Μην στέλνεις σε κωδικοποίηση MIME Quoted-Printable προς κάποιο forum Αγγλικής 
                  γλώσσας. Αυτή η κωδικοποίηση μπορεί να είναι απαραίτητη όταν γράφεις σε κάποια 
                  γλώσσα που δεν καλύπτει το ASCII, αλλά πολλά προγράμματα αλληλογραφίας δεν το 
                  υποστηρίζουν. Όταν χαλάνε, όλα αυτά τα =20 ιερογλυφικά που είναι διασκορπισμένα
                  μέσα στο κείμενο είναι άσχημα και αποσπούν την προσοχή — ή μπορεί να υπονομεύσουν
                  ενεργά την σημασιολογία του κειμένου σου.
                </p>
              </li>
              <li>
                <p>
                  Ποτέ, <span class=
                  "emphasis"><em>μα ποτέ</em></span> μην περιμένεις από τους hackers να έχουν την
                  δυνατότητα να διαβάσουν κλειστές ιδιόκτητες μορφές εγγράφων όπως το Microsoft Word
                  ή το Excel. Οι περισσότεροι hackers αντιδρούν σε αυτά περίπου όσο καλά όσο θα 
                  αντιδρούσες αν σου έριχναν ένα φορτηγό από αχνιστή χοιρινή κοπριά μπροστά από την
                  εξώπορτά σου. Ακόμη και όταν μπορούν να συνεργαστούν, αγανακτούν με το ότι πρέπει
                  να το κάνουν.
                </p>
              </li>
              <li>
                <p>
                  Αν στέλνεις ένα e-mail από ένα μηχάνημα με Windows,
                  απενεργοποίησε το ηλίθιο χαρακτηριστικό των "<span class="quote">Smart
                  Quotes</span>" της Microsoft. Κάν' το έτσι ώστε να αποτρέψεις άχρηστους 
                  χαρακτήρες από το να αναπηδούν μέσα στο mail σου.
                </p>
              </li>
              <li>
                <p>
                  Στα forums του Παγκόσμιου Ιστού, μην καταχράσαι τις δυνατότητες για
                  "<span class=
                  "quote">smiley</span>" και για
                  "<span class=
                  "quote">HTML</span>" (όταν υπάρχουν). Ένα χαμογελάκι ή και δύο είναι συνήθως 
                  εντάξει, αλλά το χρωματιστό φανταχτερό κείμενο κάνει τους άλλους να σκέφτονται
                  πως είσαι μπούφος. Σοβαρά, το να χρησιμοποιείς υπερβολικά smileys και χρώμα και
                  γραμματοσειρές θα σε κάνει να φαίνεσαι σαν μία χαζοχαρούμενη έφηβη κοπελίτσα, 
                  που γενικά δεν είναι μία καλή ιδέα εκτός αν σε ενδιαφέρουν περισσότερο 
                  απαντήσεις σχετικά με το sex.
                </p>
              </li>
            </ul>
          </div>
          <p>
            Αν χρησιμοποιείς ένα πρόγραμμα αλληλογραφίας με γραφικό περιβάλλον όπως το Netscape 
            Messenger, το MS Outlook, ή τα συναφή, πρόσεξε ότι μπορεί παραβαίνει αυτούς τους κανόνες
            όταν χρησιμοποιείται με τις προεπιλεγμένες του ρυθμίσεις. Πολλά τέτοια προγράμματα έχουν
            κάποια εντολή "<span class="quote">Προβολή Πηγαίου Κώδικα</span>". Χρησιμοποίησέ το 
            πάνω σε κάτι στον φάκελο απεσταλμένα, επιβεβαιώνοντας ότι στέλνει το σκέτο κείμενο χωρίς
            άχρηστες σάλτσες.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="beprecise" name="beprecise"></a>Να είσαι ακριβής και περιγραφικός σχετικά με το πρόβλημά σου
                </h3>
              </div>
            </div>
          </div>
          <div class="itemizedlist">
            <ul>
              <li>
                <p>
                  Περίγραψε τα συμπτώματα του προβλήματός ή του σφάλματός σου προσεκτικά και 
                  ξεκάθαρα.
                </p>
              </li>
              <li>
                <p>
                  Περίγραψε το περιβάλλον μέσα στο οποίο συμβαίνει
                  (μηχάνημα, Λειτουργικό Σύστημα, εφαρμογή, οτιδήποτε). Να παρέχεις την διανομή
                  του προμηθευτή σου καθώς και το επίπεδο έκδοσης
                  (π.χ.: "<span class=
                  "quote">Fedora Core 4</span>",
                  "<span class="quote">Slackware
                  9.1</span>", κλπ.).
                </p>
              </li>
              <li>
                <p>
                  Περίγραψε την έρευνα που έκανες για να προσπαθήσεις να κατανοήσεις το πρόβλημα
                  πριν να κάνεις την ερώτηση.
                </p>
              </li>
              <li>
                <p>
                  Περίγραψε τα διαγνωστικά βήματα που έλαβες μόνος σου για να προσπαθήσεις να 
                  απομονώσεις το πρόβλημα πριν να κάνεις την ερώτηση.
                </p>
              </li>
              <li>
                <p>
                  Περίγραψε οποιεσδήποτε πιθανές αλλαγές στον υπολογιστή σου ή στις ρυθμίσεις του
                  λογισμικού σου.
                </p>
              </li>
              <li>
                <p>
                  Αν είναι με κάποιον τρόπο δυνατό, προμήθευσε έναν τρόπο να <em>επαναδημιουργηθεί 
                  το πρόβλημα μέσα σε ένα ελεγχόμενο περιβάλλον</em>.
                </p>
              </li>
            </ul>
          </div>
          <p>
            Κάνε ό,τι καλύτερο μπορείς για να προβλέπεις τις ερωτήσεις που θα κάνει ένας hacker, και
            απάντησέ τις εκ των προτέρων στην αίτησή σου για βοήθεια.
          </p>
          <p>
            Το να δώσεις στους hackers την δυνατότητα να επαναδημιουργήσουν το πρόβλημα σε ένα
            ελεγχόμενο περιβάλλον είναι ιδιαίτερα σημαντικό αν αναφέρεις κάτι που νομίζεις ότι είναι
            ένα σφάλμα στον κώδικα. Όταν το κάνεις αυτό, οι πιθανότητες να λάβεις μία χρήσιμη 
            απάντηση και η ταχύτητα με την οποία θα λάβεις αυτή την απάντηση θα βελτιωθούν και οι
            δύο δραματικά.
          </p>
          <p>
            Ο Simon Tatham έχει γράψει ένα εξαιρετικό δοκίμιο επονομαζόμενο
            <a href=
            "http://www.chiark.greenend.org.uk/~sgtatham/bugs.html"
           >How to Report Bugs Effectively</a>. Σου συστήνω ανεπιφύλακτα να το 
            διαβάσεις.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="volume" name="volume"></a>Η ποσότητα δεν συνεπάγεται ακρίβεια
                </h3>
              </div>
            </div>
          </div>
          <p>
            Πρέπει να είσαι ακριβής και περιγραφικός. Αυτό δεν επιτυγχάνεται απλώς με το να ρίξεις
            τεράστιες ποσότητες κώδικα ή δεδομένων σε μία αίτηση για βοήθεια. Αν έχεις έναν μεγάλο
            και περίπλοκο τρόπο που κρασάρει κάποιο πρόγραμμα, προσπάθησε να τον περικόψεις και να 
            τον κάνεις όσο το δυνατόν συντομότερο.
          </p>
          <p>
            Αυτό είναι χρήσιμο για τουλάχιστον τρεις λόγους. Πρώτον: το να είναι φανερό πως επενδύεις
            κόπο στο να απλοποιήσεις την ερώτηση το κάνει πιθανότερο να λάβεις απάντηση, Δεύτερον: 
            το να απλοποιήσεις την ερώτηση το κάνει πιθανότερο να λάβεις μία 
            <span class="emphasis"><em>χρήσιμη</em></span> απάντηση.
            Τρίτον: Κατά την διάρκεια του ξεκαθαρισμού της αναφοράς σφάλματός σου, μπορεί να 
            αναπτύξεις έναν τρόπο διόρθωσης ή κάποιον τρόπο να αποφύγεις το πρόβλημα ο ίδιος.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="id306617" name="id306617"></a>Μην σπεύδεις να υποστηρίξεις ότι βρήκες 
                  κάποιο σφάλμα
                </h3>
              </div>
            </div>
          </div>
          <p>
            Όταν έχεις προβλήματα με ένα λογισμικό, μην υποστηρίζεις ότι έχεις βρει ένα σφάλμα
            εκτός και αν είσαι
            <span class="emphasis"><em>πάρα</em></span> πολύ σίγουρος για το πού βαδίζεις. 
            Συμβουλή: εκτός και αν μπορείς να προμηθεύσεις ένα patch πηγαίου κώδικα που διορθώνει 
            το πρόβλημα, ή μία δοκιμή πάνω σε μία προηγούμενη έκδοση η οποία να επιδεικνύει ότι 
            υπάρχει λανθασμένη συμπεριφορά, κατά πάσα πιθανότητα δεν είσαι αρκετά σίγουρος. Αυτό 
            εφαρμόζεται και στις ιστοσελίδες και στην τεκμηρίωση, επίσης — αν έχεις βρει ένα 
            "<span class=
            "quote">σφάλμα</span>" στην τεκμηρίωση, πρέπει να προμηθεύσεις ένα κείμενο για να 
            αντικαταστήσει το παλιό και τις σελίδες στις οποίες θα έπρεπε να μπει.
          </p>
          <p>
            Θυμήσου, υπάρχουν πολλοί άλλοι χρήστες που δεν αντιμετωπίζουν το πρόβλημά σου. Αλλιώς θα
            το μάθαινες όταν θα διάβαζες την τεκμηρίωση ή όταν θα έψαχνες το Διαδίκτυο (το έκανες
            αυτό πριν παραπονεθείς,
            <a href="#before" title="Before You Ask">έτσι δεν είναι</a>;). Αυτό σημαίνει ότι πολύ
            πιθανόν να κάνεις εσύ κάτι λάθος, όχι το λογισμικό.
          </p>
          <p>
            Οι άνθρωποι που αναπτύσσουν το λογισμικό δουλεύουν πολύ σκληρά για να το κάνουν να 
            δουλεύει όσο καλύτερα γίνεται. Αν υποστηρίξεις ότι βρήκες κάποιο σφάλμα, θα 
            αμφισβητήσεις την εμπιστοσύνη τους, κάτι που μπορεί να προσβάλλει κάποιους από αυτούς ακόμη
            και αν έχεις δίκιο.
            Είναι ειδικά μη διπλωματικό να φωνάζεις
            "<span class=
            "quote">σφάλμα</span>" στην γραμμή Θέματος.
          </p>
          <p>
            Όταν κάνεις την ερώτησή σου, είναι καλύτερο να γράφεις σαν να υποθέτεις ότι 
            <span class=
            "emphasis"><em>εσύ</em></span> κάνεις κάτι λάθος, ακόμη και αν προσωπικά είσαι αρκετά
            σίγουρος ότι έχεις βρει ένα ουσιαστικό σφάλμα. Αν στ' αλήθεια είναι σφάλμα, θα το 
            ακούσεις στην απάντηση. Παίξε το παιχνίδι έτσι ώστε οι διαχειριστές να θέλουν να σου 
            απολογηθούν αν το σφάλμα είναι αληθινό, αντί να τους χρωστάς μία συγγνώμη αν τα έκανες
            σαλάτα.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="id306775" name="id306775"></a>Η υποβολή δεν αντικαθιστά την εργασία για το σπίτι
                </h3>
              </div>
            </div>
          </div>
          <p>
            Κάποιοι άνθρωποι που καταλαβαίνουν ότι δεν πρέπει να συμπεριφέρονται με αγένεια ή 
            αλαζονικά, απαιτώντας μία απάντηση, κουρνιάζουν στο αντίθετο άκρο, αυτό της υποβολής.
            "<span class="quote">Το ξέρω πως είμαι ένας αξιοθρήνητος αρχάριος και άχρηστος, 
            αλλά...</span>".
            Αυτό αποσπά την προσοχή και δεν βοηθά. Είναι ειδικά ενοχλητικό όταν συνοδεύεται από 
            ασάφεια σχετικά με το ουσιαστικό πρόβλημα.
          </p>
          <p>
            Μην σπαταλάς τον χρόνο σου, ή τον δικό μας, με λόγια αρχιεπισκοπικού κηρύγματος. 
            Αντιθέτως, παρουσίασε τα σχετιζόμενα γεγονότα και την ερώτησή σου όσο πιο ξεκάθαρα 
            μπορείς. Αυτός είναι ένας καλύτερος τρόπος να τοποθετήσεις τον εαυτό σου από το να 
            προσκυνήσεις υποβάλλοντας τα σέβη σου.
          </p>
          <p>
            Μερικές φορές τα forums του Παγκόσμιου Ιστού έχουν ξεχωριστά μέρη για τις ερωτήσεις 
            των αρχάριων. Αν νιώθεις πως έχεις μία αρχάρια ερώτηση, απλώς πήγαινε εκεί. Αλλά μην 
            υποβάλλεσαι ούτε κι εκεί.
          </p>
        </div>
        <div class="sect2" lang="el" xml:lang="el">
          <div class="titlepage">
            <div>
              <div>
                <h3 class="title">
                  <a id="symptoms" name="symptoms"></a>Περίγραψε τα συμπτώματα του προβλήματος, όχι τις εικασίες σου
                </h3>
              </div>
            </div>
          </div>
          <p>
            Δεν χρησιμεύει να πεις στους hackers τι νομίζεις ότι προκαλεί το πρόβλημά σου. 
            (Αν οι διαγνωστικές σου θεωρίες ήταν τόσο ουσιώδεις, θα συμβουλευόσουν άλλους για 
            βοήθεια;) Έτσι λοιπόν, σιγουρέψου πως τους λες χύμα τα συμπτώματα του τι πάει στραβά, 
            αντί για τις διερμηνείες και θεωρίες σου. Άσ' τους να κάνουν τις διερμηνείες και την
            διάγνωση. Αν νιώθεις πως είναι σημαντικό να αναφέρεις την εικασία σου, κατηγοριοποίησέ
            την ξεκάθαρα ως τέτοια και περίγραψε γιατί αυτή η απάντηση δεν σου δουλεύει.
          </p>
          <div class="variablelist">
            <dl>
              <dt>
                <span class="term"><span class=
                "strong"><strong>Ηλίθιο:</strong></span></span>
              </dt>
              <dd>
                <p>
                  Λαμβάνω συνεχόμενα σφάλματα SIG11 όταν μεταγλωττίζεται ο πυρήνας, και υποθέτω πως
                  υπάρχει μία ελάχιστη ρωγμή πάνω σε κάποιο ίχνος της μητρικής. Ποιος είναι ο 
                  καλύτερος τρόπος να ελέγξω για κάτι τέτοιο;
                </p>
              </dd>
              <dt>
                <span class="term"><span class=
                "strong"><strong>Έξυπνο:</strong></span></span>
              </dt>
              <dd>
                <p>
                  Ο υπολογιστής K6/233 που έφτιαξα μόνος μου με μία μητρική FIC-PA2007
                  (με chipset VIA Apollo VP2) και 256MB Corsair PC133
                  SDRAM αρχίζει να λαμβάνει συχνά σφάλματα SIG11 περίπου 20 λεπτά αφού ανοίξω τον 
                  υπολογιστή κατά την διάρκεια της μεταγλώττισης του πυρήνα, αλλά ποτέ μέσα στα πρώτα
                  20 λεπτά. Η επανεκκίνηση δεν βοηθάει, μόνο αν κλείσω τον υπολογιστή
                  την νύχτα. Δοκίμασα να αλλάξω τις RAM αλλά δεν βοήθησε. Ακολουθεί το σχετιζόμενο
                  τμήμα από το log της μεταγλώττισης.
                </p>
              </dd>
            </dl>
          </div>
          <p>
            Καθώς το προαναφερθέν είναι δύσκολο να το πιάσουν πολλοί άνθρωποι, να μία φράση για να 
            σου το θυμίζει:
            "Όλες οι ειδικοί στις διαγνώσεις είναι από το Missouri." Το σύνθημα αυτής της πολιτείας της Αμερικής
